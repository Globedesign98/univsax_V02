          <p>Since 2005, Lars Mlekusch is professor for  saxophone and chamber music at the Konservatorium Wien University where he is  leading an international saxophone studio. From 2004 to 2007 he was also  teaching saxophone at the University of Music in Basel. Mlekusch has been  teaching Masterclasses at several conservatories and music universities throughout  the world (e.g in Amsterdam, Utrecht, Basel, Lucerne, Brussels, Milan, Rome, Ljubljana, Madrid, Sevilla, Boulogne-Billancourt, Palma  de Mallorca, Bangkok and many more). For 2011 he has been invited to be part of  the jury at the International Saxophone Competition in Nova Gorica (Slovenia)  and at the 3rd International Jean-Marie Londeix competition in  Bangkok.</p>

