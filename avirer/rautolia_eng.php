        Joonatan Rautiola was born in 1983 in Helsinki, Finland. He studied the saxophone at the Sibelius Academy under Pekka Savijoki and in Paris with Nicolas Prost and Christian Wirth. In 2010 he became the first Nordic saxophonist having graduated from Claude Delangle's saxophone class at the Paris Conservatory. He completed his studies at Hoch Hochschule f&uuml;r Musik und Tanz K&ouml;ln with Daniel Gauthier and privately with clarinetist Paul Meyer and legendary oboist Maurice Bourgue. <br>

        <br>

Joonatan Rautiola has appeared as a soloist with the Latvian National Symphony Orchestra, Finnish Radio Symphony Orchestra, D&uuml;sseldorfer Symfoniker, Mecklenburgische Staatskapelle Schwerin, Het Kamerorkest Brugge, Tapiola Sinfonietta, Kymi Sinfonietta, Joensuu City Orchestra, St. Michel Strings and with several professional Nordic wind orchestras. He has given recitals in London, Dublin, St. Petersbourg and at New York's Carnegie Hall. <br>

<br>

An active performer of contemporary music, Joonatan Rautiola has given first performances of works by numerous composers and has received direct tuition from Pierre Boulez and Betsy Jolas. His recent premiere of Olli Virtaperko's baritone saxophone concerto "Multikolor" with TampereRaw Ensemble (Tampere, October 2016) was extremely well received by critics and by the concert audience. In 2015, Joonatan Rautiola premiered Christian Lauba's and Antonin Servi&egrave;re's works for solo saxophone as well as Guillaume Connesson's saxophone concerto with the Strasbourg Philharmonic Orchestra. <br>

<br>

Joonatan Rautiola is the only wind instrumentist having won the Finnish National Radio's Young Soloists' Competition. His numerous prizes in international competitions include first prizes at the Nordic Wind Soloists' Competition and the Nova Gorica International Saxophone Competition as well as prizes at the International Aeolus Competition for Wind Instruments and the International Adolphe Sax Saxophone Competition in Dinant, Belgium. <br>

<br>

Joonatan Rautiola teaches at the Sibelius Academy, Helsinki, and gives masterclasses at Tokyo, Riga, London, M&uuml;nchen and Strasbourg among others. <br>

