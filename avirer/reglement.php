        <td class="txt_blc12"><div align="justify"><strong>Article 1.</strong><br />
  La 29ème édition de l'Université Européenne de Saxophone se déroulera dans la ville de Gap du 16 au 27 juillet 2018.<br />
  <br />
  <strong>Article 2.</strong><br />
  L'Université Européenne de Saxophone est ouverte aux saxophonistes de toutes nationalités, à condition qu'ils soient âgés au minimum de 18 (dix-huit) ans avant le 16 juillet 2018. Toutefois les étudiants mineurs (âgés de moins de 18 ans) sont autorisés à participer à l'Université Européenne de Saxophone à condition d'être accompagnés par leurs parents ou par un des responsables légaux. Ils pourront ainsi suivre les cours, les cours publics et les concerts et participer aux représentations des étudiants mais ne seront pas hébergés dans les locaux mis à la disposition de l'Université, ni pris en charge par le personnel encadrant. En aucun cas la responsabilité de L'université Européenne de Saxophone ne saura être engagée en cas de problème que rencontrerait un étudiant mineur durant le déroulement du stage.<br />
  <br />
  <strong>Article 3.</strong><br />
  Pour s'inscrire à l'Université Européenne de Saxophone, les étudiants doivent avoir un niveau de fin d'études,  pré professionnel du type « CEM  ou DEM» en CRD (Conservatoire à Rayonnement Départemental) ou en CRR (Conservatoire à Rayonnement Régional) ou équivalent. Ils doivent être capables d'interpréter, à titre d'exemple « Tableaux de Provence » de Paule Maurice, ou « Sonata » de Paul Creston.<br />
  <br />
  <strong>Article 4.</strong><br />
  L'Université Européenne de Saxophone accueille un maximum de 54 (cinquante-quatre) étudiants ; elle se réserve le droit de refuser toute candidature si les conditions de participation ne sont pas respectées.<br />
  <br />
  <strong>Article 5.</strong><br />
  Les professeurs de saxophone titulaires du CA ou du DE, et toute autre personne intéressée (étudiant, particulier) peuvent s'inscrire à l'Université Européenne de Saxophone en tant qu'auditeur et assister aux cours publics, aux concerts et aux cours qui ont lieu durant le stage. L'Université Européenne de Saxophone ne pourra fournir de solution d'hébergement aux auditeurs libres, mais ils auront par contre la possibilité de manger matin, midi et soir avec les étudiants, en contrepartie d'un paiement à l'unité pour chaque repas. Le prix fixé est de 8.90 Euros (huit Euro et quatre vingt dix centimes) pour chaque repas et de 7.00 Euros (sept Euros) par petit-déjeuner.<br />
  <br />
  <strong>Article 6.</strong><br />
  La clôture des inscriptions pour la 29ème Université Européenne de Saxophone est fixée au 9 (neuf) juin 2018.<br />
  <br />
  <strong>Article 7.</strong><br />
  En cas de désistement après la validation d'une inscription, l'Université Européenne de Saxophone procédera à un remboursement des frais d'inscription, amputés de 130 (cent trente) Euros de frais de dossier. À compter du 12 (douze) juin, plus aucune inscription ne sera remboursée, sauf cas de force majeure (raisons de santé ou familiales graves) et sur présentation de justificatifs.<br />
  <br />
  <strong>Article 8.</strong><br />
  Le paiement de l'intégralité du forfait est obligatoire afin de confirmer l'inscription. L'Université Européenne de Saxophone n'accepte pas de paiement différé, ou effectué en plusieurs parties.<br />
  <br />
  <strong>Article 9.</strong><br />
  Après réception du paiement de l'intégralité du forfait, les étudiants recevront par courriel une confirmation de leur inscription effective à l'Université Européenne de Saxophone. Celle-ci deviendra alors définitive.<br />
  <br />
  <strong>Article 10.</strong><br />
  Pour les paiements par cartes, les frais relatifs aux différentes opérations bancaires seront à régler en même temps que le montant de l'inscription par une somme forfaitaire de 38 (trente huit) Euros.<br />
  <br />
  <strong>Article 11.</strong><br />
  Les étudiants sujets à des problèmes d'allergie(s), d'asthme, ou à d'autres problèmes de santé, doivent venir avec une fiche sanitaire récente signée de leur médecin traitant, et la remettre à l'équipe de l'Université Européenne de Saxophone le premier jour, et ce pour des raisons de sécurité en cas d'incident. Ceux qui sont végétariens doivent également le préciser sur le formulaire d'inscription, afin que les responsables de la restauration puissent le prendre en compte.<br />
  <br />
  <strong>Article 12.</strong><br />
  Une grande assiduité est exigée de la part des étudiants. Ceux-ci s'engagent par leur inscription à l'Université Européenne de Saxophone à suivre l'intégralité des cours et des concerts qui leur seront proposés durant les 12 jours du stage, ainsi que les cours publics et autres cours ou conférences à caractère pédagogique assurés par un ou plusieurs professeurs de l'Université.<br />
  <br />
  <strong>Article 13.</strong><br />
  Les étudiants s'engagent à respecter les consignes d'organisation et d'emploi du temps qui leur seront communiquées par l'équipe organisatrice le premier jour du stage. Les étudiants s'engagent à respecter les règles élémentaires de vie commune dans les locaux qui seront mis à leur disposition : respect du silence au sein du foyer, interdiction totale de jouer du saxophone dans les chambres, interdiction totale d'y introduire des produits alcoolisés ou des substances prohibées par la loi en vigueur en France. <br>
L'Université Européenne de Saxophone se réserve le droit de sanctionner tout étudiant et auditeur libre pour tout comportement, tenu pendant les heures de stages ou les manifestations ou à l'occasion de temps libres, qui serait préjudiciable aux intérêts de l'Association et qui pourrait nuire à l'image de celle-ci (état alcoolique, violence verbale ou corporelle, consommation de stupéfiants ou produits illicites ou tout comportement inapproprié).<br>
En cas de manquement aux règles mentionnées ci-dessus l'étudiant ou l'auditeur libre concerné sera entendu par le Président de l'association ou son représentant, le Directeur artistique ou son représentant et l'organisateur ou son représentant.<br>
Ces derniers entendent l'étudiant ou l'auditeur concerné et, pour éclairer leurs décisions, peuvent consulter toute autre personne liée aux faits dont il est question.
Selon la gravité des faits, les sanctions disciplinaires peuvent être :<br>
- l'exclusion temporaire de 2 jours de cours individuels consécutifs à compter de la décision sans remboursement des frais pédagogiques<br>
- l'exclusion définitive du stage sans remboursement des frais pédagogiques et d'hébergement.<br>
 Cette  décision est sans appel.
<br />
  <br />
  <strong>Article 14.</strong><br />
  Les étudiants de l'Université Européenne de Saxophone s'engagent à respecter tout le matériel et les locaux qui sont mis à leur disposition pour la durée du stage : salles de cours, réfectoire, chambres à coucher, mobilier, instruments, pupitres, et l'ensemble des locaux où ils sont amenés à passer du temps en général (établissement hôte, Conservatoire de Musique, lieux des concerts, etc.). L'organisation de l'Université Européenne de Saxophone se réserve le droit de réclamer des dédommagements financiers aux étudiants en cas de détérioration des matériels et des locaux mentionnés ci-dessus.<br />
  <br />
  <strong>Article 15.</strong><br />
  L'Université Européenne de Saxophone se réserve le droit de pratiquer tout enregistrement, audio ou vidéo, ou de faire des photographies d'un ou plusieurs étudiant(s) durant les cours ou les concerts. Les cours individuels, les cours publics et les concerts sont considérés comme des manifestations publiques.<br />
  <br />
  <strong>Article 16.</strong><br />
  Les étudiants de l'Université Européenne de Saxophone devront avoir préparé avant le début du stage un programme de 30 (trente) minutes minimum, qu'ils répéteront et travailleront durant leur cours avec les professeurs de l'Université.<br />
  <br />
  <strong>Article 17.</strong><br />
  L'usage des photocopies de partitions est, selon la loi en vigueur en France, strictement interdit durant la totalité du séjour pour tous les étudiants.<br />
  <br />
  <strong>Article 18.</strong><br />
  Les étudiants doivent avoir à disposition une tenue de concert (haut blanc et bas noir), qu'ils utiliseront lors des différentes représentations qu'ils seront amenés à faire lors du déroulement de l'Université Européenne de Saxophone ; tous les étudiants doivent également arriver munis d'un PUPITRE.<br />
  <br />
  <strong>Article 19.</strong><br />
  La participation à la 29ème édition de l'Université Européenne de Saxophone implique l'acceptation préalable de l'intégralité du présent règlement.<br />
  <br />
  <strong>Article 20.</strong><br />
  L'Université Européenne de Saxophone décline toutes responsabilités en cas perte ou de vol d'effets personnels sur les lieux du stage.<br />
  <br />
  <strong>Article 21.</strong><br />
  Seul le règlement rédigé en langue française ici présent fait foi.<br />
