                  <br>
                  The Universit&eacute; Europ&eacute;enne de Saxophone has been established in Gap in 1989 by Messrs Serge BICHON (Professor at the Conservatoire National de R&eacute;gion in Lyon), Claude DELANGLE (Professor at the Conservatoire National Sup&eacute;rieur de Musique in Paris) and Yves RAMBAUD (Professor at the Ecole Nationale de Musique in Gap). From the very beginning, they received a strong financial support from the City of Gap. <br>
                  <br>
                  Their purpose is multiple. Although Europe does not really exist yet, Messrs BICHON, DELANGLE and RAMBAUD want to promote &ndash; ahead of their time &ndash; a free flow of ideas and knowledge. They want to promote the saxophone as both a very classical and a definitely modern instrument. They want to improve the musical education and enable the teachers-to-come to provide high-quality teaching. Finally, they want to create a first class musical event that will add up to the already vast choice of summer celebrations in the city of Gap. <br>
                  To reach this ambitious goal, they will appeal to the very best saxophone teachers in France. To make it &ldquo;European&rdquo;, they decide to invite every year a non-French teacher. Thus, in the last 35 years, the students have always had the opportunity to perfect their musical technique in contact with a unique panel of teachers, coming from all European countries. <br>
                  <br>
                  Originally, the Universit&eacute; was meant for French students &ndash; however, its organisers evolved with their time, and welcomed along the years more and more foreigners. Nowadays, the majority of the students come from abroad &ndash; Japan, China, Australia, USA, Russia, and all European countries &ndash; and learn with this unique team how to become more familiar with the French style and technique, which are unanimously considered the best in the world.  <br>
                  <br>
                  Today, the first generation of these students has become professional, and they work hard on promoting way over the French borders the &ldquo;Gap model&rdquo;. Although many summer schools now offer the same concept all over Europe, the Universit&eacute; Europ&eacute;enne de Saxophone de Gap has always been one step ahead. The high-levelled teaching staff, the first class concerts, the constant innovation, and the magnificent alpine landscapes as well as the international atmosphere and the unique &ldquo;joie de vivre&rdquo; keep proving that Gap is an indispensable destination for all saxophone players around the world.<br>
