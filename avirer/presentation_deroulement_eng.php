            <td valign="top" class="txt_blc12"><div align="justify"><strong>7.30am: </strong>After they spent their night in the modern and  comfortable dormitory of Foyer des Jeunes Travailleurs, the 54 students  take their breakfast in the restaurant one minute away from their  rooms. It is recommended not to miss it, since the days are long and  pretty intense.<br>
              <br>
                <strong>From 8.45am to 12.45pm</strong>:  There are 9 different groups, each one being composed of 6 students.  Each and every one of the 54 students will have an individual 40-minute  lesson with each one of the teachers, as well as with the two pianists.  All students must have worked on a 30+ minute long piece before the  Universit&eacute;. They will work this piece with each one of the teachers,  thus receiving a varied feedback on their playing. Before or after this  daily individual lesson, the students can rehearse in one of the many  rehearsing rooms at their disposal. They can also test and try <strong>Selmer</strong>&rsquo;s  and <strong>Vandoren</strong>&rsquo;s new range of  products, or have their saxophone fixed by one of the <strong>JS Musique</strong>&rsquo;s  technicians. These three historical partners of the Universit&eacute;  Europ&eacute;enne de Saxophone usually spend 2 to 3 full days in Gap during  the unfolding of the event. <br>
                <br>
                <strong>1.00pm</strong>:  The students come back to Foyer des Jeunes Travailleurs and have lunch. Most of  the teachers eat there as well, thus allowing the students to have a  very privileged time in their company.<br>
                <br>
                <strong>AFTERNOON</strong>:  In the early afternoon, the students usually rehearse with their  respective quartet or ensemble. The teachers, who conduct the  rehearsals, form the different groups based on the students&rsquo; wishes and  level on the first day. At the end of the Universit&eacute;, the students will  perform their own concert in this group configuration. <br>
The rest  of the afternoon is generally dedicated to master-classes, given by the  teachers or by renowned saxophone experts. Finally, the students can  also spend a relaxing time and enjoy the many tourist aspects provided  by the city of Gap, as well as become acquainted with their colleagues  from all over the world. <br>
<br>
<strong>7pm30 or 8pm30</strong>:  The students have dinner at the Foyer des Jeunes Travailleurs&rsquo;s restaurant, and  get ready for the last (but not least!) event of the day&hellip;<br>
<br>
<strong>8pm30 or 9pm30</strong>:  Each in turn, evening after evening, all 7 teachers give a concert,  accompanied by the prestigious Universit&eacute; Europ&eacute;enne de Saxophone&rsquo;s  pianists, in the P&eacute;nitents Chapel, a lovely place in the heart of the  city. These free high quality concerts are enjoyed by the students, as  well as by about 200 people every evening.
